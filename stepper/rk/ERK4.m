function [A, b, c, d] = ERK4()
%ERK4 Eqn (51) from S. Krogstad. "Generalized integrating factor methods for stiff PDEs", 2005

s = 4; % number of stages 
m = 4; % number of phi functions

A = zeros(2, m, s - 1, s - 1);
b = zeros(2, m, s);
d = zeros(2, s);
c = zeros(s - 1, 1);

% -- A Matrix ------------------------------------------------------------------------ 
% A(1,1) = (1/2) \varphi_1(h/2 L)
A(:, 2, 1, 1) = [0.5, 0.5];

% A(2,1) = (1/2) \varphi_1(h/2 L) - \varphi_2(h/2 L)
A(:, 2, 1, 2) = [0.5, 0.5];
A(:, 3, 1, 2) = [-1.0,  0.5]; 

% A(2,2) = (1/2) \varphi_2(h/2 L)
A(:, 3, 2, 2) = [1.0, 0.5];

% A(3, 1) = \varphi_1(h L) - 2 \varphi_2(h L)
A(:, 2, 1, 3) = [1.0, 1.0];
A(:, 3, 1, 3) = [-2.0, 1.0];

% A(3,3) = 2 \varphi_2(h L)
A(:, 3, 3, 3) = [2.0, 1.0];

% -- b Vector ------------------------------------------------------------------------
% (\varphi_1(hL) - 3 \varphi_2(hL) + 4 \varphi_3(hL) * F(y_n)
b(:, 2, 1) = [ 1.0,  1.0 ]; 
b(:, 3, 1) = [ -3.0, 1.0 ];
b(:, 4, 1) = [ 4.0,  1.0 ]; 

% (2 \varphi_2(hL) - 4 \varphi_3(hL)) * F(Y_1)
b(:, 3, 2) = [ 2.0,  1.0 ];
b(:, 4, 2) = [ -4.0, 1.0 ];

% (2 \varphi_2(hL) - 4 \varphi_3(hL)) * F(Y_2)
b(:, 3, 3) = [ 2.0,  1.0 ];
b(:, 4, 3) = [ -4.0, 1.0 ];

% (-1 \varphi_2(hL) - 4 \varphi_3(hL)) * F(Y_3)
b(:, 3, 4) = [ -1.0, 1.0 ];
b(:, 4, 4) = [ 4.0, 1.0 ];

% -- d Vector ------------------------------------------------------------------------
d(:, 1) = [1.0,  0.5]; % exp(h/2 L) y_n
d(:, 2) = [1.0,  0.5]; % exp(h/2 L) y_n
d(:, 3) = [1.0,  1.0]; % exp(h L) y_n 
d(:, 4) = [1.0,  1.0]; % exp(h L) y_n

% -- c Vector ------------------------------------------------------------------------
c(:) = [0.5, 0.5, 1.0];

end